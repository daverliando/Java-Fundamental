public class D_ComparisonOperators {
    public static void main(String[] args) {
        int x = 7;
        int y = 3;
        
        System.out.println(x==y);
        System.out.println(x!=y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x>y);
        System.out.println(x>=y);
        
        boolean isEqual = x==y;
        System.out.println(isEqual);
    }
}
